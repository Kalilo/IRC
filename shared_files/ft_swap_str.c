#include "../includes/shared.h"

void	ft_swap_str(char **s1, char **s2)
{
	char	*tmp;

	if (s1 == NULL || s2 == NULL)
		return ;
	tmp = *s1;
	*s1 = *s2;
	*s2 = tmp;
}
